fn assert_state(spc700: &Spc700<impl Spc700Bus>, expected_state: &str) {
    let mut actual = spc700.debug().state();
    let mut expected = Spc700State::parse_mesen_trace(expected_state).unwrap();
    println!("{}", actual);
    actual.instruction.operand_str = None;
    expected.instruction.operand_str = None;
    actual.clock = Default::default();
    expected.clock = Default::default();
    pretty_assertions::assert_eq!(actual.to_string(), expected.to_string());
}

fn assert_states(spc700: &mut Spc700<impl Spc700Bus>, expected_states: &[&str]) {
    for expected_state in expected_states {
        assert_state(spc700, expected_state);
        spc700.step();
    }
}

#[test]
fn boot_rom_transfer_test() {
    let mut spc700 = Spc700::new(ApuBus::new(mock_collector()), mock_collector());
    assert_states(&mut spc700, INIT_TRACE);

    // Init done signal is 0xaabb on port 0-1
    assert_eq!(spc700.bus.channel_out[0], 0xaa);
    assert_eq!(spc700.bus.channel_out[1], 0xbb);

    // Magic ready number
    spc700.bus.channel_in[0] = 0xcc;
    spc700.bus.channel_in[1] = 0xcc;
    // Target address 0x0002
    spc700.bus.channel_in[2] = 0x00;
    spc700.bus.channel_in[3] = 0x02;

    // SPC700 executes this code:
    // Start:  MOVW YA,$F6    ; *** MAIN LOOP ***
    //         MOVW $00,YA    ; Get address from 5A22's $2142-3,
    //         MOVW YA,$F4    ; mode from $2141, and echo $2140 back
    //         MOV $F4,A
    //         MOV A,Y
    //         MOV X,A
    assert_states(
        &mut spc700,
        &[
            "FFCF  CMP $F4,#$CC [CPUIO0] = $CC      A:00 X:00 Y:00 S:EF P:nvpbhiZc V:162 H:1172 F:4",
            "FFD2  BNE $FFCF                        A:00 X:00 Y:00 S:EF P:nvpbhiZC V:162 H:1276 F:4",
            "FFD4  BRA $FFEF                        A:00 X:00 Y:00 S:EF P:nvpbhiZC V:162 H:1320 F:4",
            "FFEF  MOVW YA,$F6 [CPUIO2] = $0200     A:00 X:00 Y:00 S:EF P:nvpbhiZC V:163 H:36   F:4",
            "FFF1  MOVW $00,YA [$0000] = $006D      A:00 X:00 Y:02 S:EF P:nvpbhizC V:163 H:144  F:4",
            "FFF3  MOVW YA,$F4 [CPUIO0] = $CCCC     A:00 X:00 Y:02 S:EF P:nvpbhizC V:163 H:248  F:4",
            "FFF5  MOV $F4,A [CPUIO0] = $CC         A:CC X:00 Y:CC S:EF P:NvpbhizC V:163 H:352  F:4",
            "FFF7  MOV A,Y                          A:CC X:00 Y:CC S:EF P:NvpbhizC V:163 H:436  F:4",
            "FFF8  MOV X,A                          A:CC X:00 Y:CC S:EF P:NvpbhizC V:163 H:476  F:4",
        ],
    );
}

/// Validates the spc cycle timing versus the master clock during initial spc700 boot.
/// This is a little tricky, since a bunch of spc700 instructions are executed at once
/// during reset before the standard emulation loop starts.
#[test]
fn boot_rom_spc_cycle_accuracy() {
    logging::test_init(true);

    let mut spc700 = Spc700::new(ApuBus::new(mock_collector()), mock_collector());
    for (pc, spc_cycle, _) in INIT_TIMING {
        let state = spc700.debug().state();
        println!("(0x{pc:04X} {spc_cycle}): {state}");
        assert_eq!(state.instruction.address, AddressU16(*pc));
        assert_eq!(state.spc_cycle, *spc_cycle);
        spc700.step();
    }
}

#[test]
fn boot_rom_master_clock_accuracy() {
    logging::test_init(true);

    // Skip the first two entries, which are before the SPC700 starts executing.
    let mut spc700 = Spc700::new(ApuBus::new(mock_collector()), mock_collector());

    // Each call to catch_up_to_master_clock advances the SPC700. Afterwards we will end up on the PC for
    // the next instruction. See INIT_TIMING for the sequence.
    let clock_pc_pairs = &[(186, 0xFFC5), (192, 0xFFC6), (276, 0xFFC7), (316, 0xFFC5)];
    for (master_clock, pc) in clock_pc_pairs {
        spc700.catch_up_to_master_clock(*master_clock);
        assert_eq!(spc700.debug().state().instruction.address, AddressU16(*pc));
    }
}

